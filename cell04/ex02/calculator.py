def main():
    a = int(input("Give me the first number : "))
    b = int(input("Give me the second number: "))

    print("Thank you !")

    print(f"{a} + {b} = {a + b}")
    print(f"{a} - {b} = {a - b}")
    print(f"{a} / {b} = {a / b}")
    print(f"{a} * {b} = {a * b}")

if __name__ == "__main__":
    main()